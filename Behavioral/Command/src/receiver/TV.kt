package receiver

class TV {
    fun turnOn() {
        println("TV was turned on")
    }

    fun turnOff() {
        println("TV was turned off")
    }

    fun watchMovie() {
        println("Started watching movie")
    }

    fun stopMovie() {
        println("Stopped watching movie")
    }

    fun increaseVolume() {
        println("Volume was increased")
    }

    fun decreaseVolume() {
        println("Volume was decreased")
    }
}